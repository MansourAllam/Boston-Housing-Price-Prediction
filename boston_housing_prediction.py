# Boston_Housing_Prediction.ipynb

from boston_housing_model import BostonHousingModel, load_boston_data

# Load and preprocess the dataset
X_train, X_test, y_train, y_test = load_boston_data()

# Initialize and train the model
model = BostonHousingModel(input_dim=X_train.shape[1])
model.train(X_train, y_train, epochs=100, batch_size=10)

# Evaluate the model
loss, mae = model.evaluate(X_test, y_test)
print(f"Mean Squared Error (MSE): {loss}")
print(f"Mean Absolute Error (MAE): {mae}")